def _parse_args(argv: Optional[list[str]] = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(description="AetherCloud UVT margin harness")
    p.add_argument("--tier", required=True, choices=["free", "solo", "pro", "team"])
    p.add_argument("--users", type=int, default=10)
    p.add_argument("--days", type=int, default=30)
    p.add_argument(
        "--persona-mix", default="casual=0.6,power=0.35,abusive=0.05",
        help="comma-separated, e.g. 'casual=0.6,power=0.35,abusive=0.05'",
    )
    p.add_argument("--seed", type=int, default=42)
    p.add_argument("--out", default="reports/")
    p.add_argument("--quiet", action="store_true")
    return p.parse_args(argv)


def main(argv: Optional[list[str]] = None) -> int:
    args = _parse_args(argv)
    mix = parse_persona_mix(args.persona_mix)
    out_dir = Path(args.out)
    if not args.quiet:
        print(f"[harness] AetherCloud UVT margin harness")
        print(f"  tier={args.tier} users={args.users} days={args.days}")
        print(f"  persona-mix={mix} seed={args.seed}")
        print(f"  writing to {out_dir}/")

    csv_path, md_path, records = run_simulation(
        tier=args.tier, users=args.users, days=args.days,
        persona_mix=mix, seed=args.seed, out_dir=out_dir, quiet=args.quiet,
    )

    if not args.quiet:
        print(f"\n[harness] OK - {len(records)} calls recorded")
        print(f"  CSV:  {csv_path}")
        print(f"  REP:  {md_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
