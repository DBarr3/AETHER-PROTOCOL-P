"""
Stage I integration harness CLI.

Usage:
    python -m aether.harness.simulate \
        --tier pro --users 25 --days 30 \
        --persona-mix casual=0.6,power=0.35,abusive=0.05 \
        --seed 42 --out reports/

What runs:
    1. Minimal FastAPI app with uvt_router mounted (same pattern as
       tests/test_uvt_routes.py — avoids importing the full api_server,
       which has a pre-existing security.prompt_guard issue in this worktree).
    2. session_mgr shim maps per-user bearer tokens → emails.
    3. In-memory Supabase client seeded with plans + N users on the
       requested tier, injected into uvt_routes + Router.
    4. TokenAccountant's httpx.AsyncClient is patched to use a
       FakeAnthropicTransport — deterministic, never hits real Anthropic.
    5. Per-user traffic generated by UserProfile (persona-driven) day-by-day.
    6. Every call's response recorded. End-of-run reconciles /account/usage
       against the harness tally and emits CSV + Markdown.

Aether Systems LLC — Patent Pending
"""

